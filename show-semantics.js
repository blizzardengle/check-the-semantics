const stats = {
    total: 0
};

function showIfNoneFound(html) {
    if (html.length < 1) {
        return '<tr><td colspan="2">None found.</td></tr>';
    }
    return html;
}

// https://javascript.plainenglish.io/walking-the-dom-7a1123fb67da
function visitNodesRecursively(node, callback) {
    callback.call(null, node);
    if (node.firstChild) { visitNodesRecursively(node.firstChild, callback); }
    if (node.nextSibling) { visitNodesRecursively(node.nextSibling, callback); }
}

function proccesNode(node) {
    const name = node.nodeName.toUpperCase();
    if (semantics[name]) {
        stats.total++;
        if (stats[name]) {
            stats[name] += 1;
            return;
        }
        stats[name] = 1;
    }
}

visitNodesRecursively(window.document, proccesNode);

